
package Entity;

public class StudentPerformance {
    private int studentRegNo;
    private int studentId;
    private int numOfAccepted;
    private int numOfWrong;
    private int sumOfScores;
    private int counter;

    public int getCounter() {
        return counter;
    }

    public void setCounter(int counter) {
        this.counter = counter;
    }

    public int getStudentRegNo() {
        return studentRegNo;
    }

    public void setStudentRegNo(int studentRegNo) {
        this.studentRegNo = studentRegNo;
    }

    public int getStudentId() {
        return studentId;
    }

    public void setStudentId(int studentId) {
        this.studentId = studentId;
    }

    public int getNumOfAccepted() {
        return numOfAccepted;
    }

    public void setNumOfAccepted(int numOfAccepted) {
        this.numOfAccepted = numOfAccepted;
    }

    public int getNumOfWrong() {
        return numOfWrong;
    }

    public void setNumOfWrong(int numOfWrong) {
        this.numOfWrong = numOfWrong;
    }

    public int getSumOfScores() {
        return sumOfScores;
    }

    public void setSumOfScores(int sumOfScores) {
        this.sumOfScores = sumOfScores;
    }
    
    
}
